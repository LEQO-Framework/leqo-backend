import asyncio
from io import UnsupportedOperation
from typing import override
from uuid import uuid4

from app.enricher import Constraints, EnricherStrategy, EnrichmentResult
from app.main import get_enricher
from app.model.CompileRequest import (
    CompileRequest,
    IfThenElseNode,
    ImplementationNode,
    MetaData,
    PassNode,
)
from app.model.CompileRequest import Node as FrontendNode
from app.processing import Processor


class IfElseEnricherStrategy(EnricherStrategy):
    @staticmethod
    async def collect_async_nodes(processor: Processor) -> list[ImplementationNode]:
        return [node async for node in processor.enrich()]

    @override
    def _enrich_impl(
        self,
        node: FrontendNode,
        constraints: Constraints | None,
    ) -> EnrichmentResult | list[EnrichmentResult]:
        if not isinstance(node, IfThenElseNode):
            return []

        if constraints is None:
            msg = "if-else-node requires constraints."
            raise UnsupportedOperation(msg)

        graphes = []
        if_node = PassNode(id=f"autogenerated_pass_node_{uuid4().hex}")
        endif_node = PassNode(id=f"autogenerated_pass_node_{uuid4().hex}")
        for nested_block in (node.thenBlock, node.elseBlock):
            nested_block.nodes.extend([if_node, endif_node])
            for edge in nested_block.edges:
                if edge.source[0] == node.id:
                    edge.source = (if_node.id, edge.source[1])
                if edge.target[0] == node.id:
                    edge.target = (endif_node.id, edge.target[1])

            compile = CompileRequest(
                metadata=MetaData(
                    version="",
                    name="",
                    description="",
                    author="",
                    optimizeWidth=constraints.optimizeWidth,
                    optimizeDepth=constraints.optimizeDepth,
                ),
                nodes=nested_block.nodes,
                edges=nested_block.edges,
            )
            processor = Processor(compile, get_enricher())
            _ = asyncio.run(self.collect_async_nodes(processor))
            graphes.append(processor.graph)
        return []
